#relational operators
num1=50
num2=25
print(num1 == num2)
print(num1 != num2)
print(num1 >= num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 < num2)
#assignment operators
a=60
a += 5
print("a:  ",a)

#logical  operators
a1 = True
a2 = False
print(not a1)
print("and: ",a1 and a2)
print("or: ",a1 or a2)